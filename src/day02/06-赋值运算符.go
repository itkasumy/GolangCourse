package main

import "fmt"

func main()  {
	/*
			赋值运算符： =,+=, -=, *=, /=, %= (掌握)
								<<=, >>=, &=, |=, ^=（了解）
		=： 把=右边的值或者是表达式的运算结果赋值给=左边
		其他赋值运算符相当于等号左边的变量和等号右边的值参与运算（等号旁边的那个运算符）的结果再赋值给自己
	*/
	res0601 := 1 + 2
	fmt.Println(res0601)

	num0601 := 2
	num0601 += 2 // 它相当于num1 = num1 + 2
	num0601 -= 1 // 它相当于 num1 = num1 - 1
	fmt.Println(num0601)

	num0601 %= 3 // num1 = num1 % 3 = 2 % 3 => 2

	num0602 := 2
	num0602 = num0602 + 2
	num0602 += 2
}
